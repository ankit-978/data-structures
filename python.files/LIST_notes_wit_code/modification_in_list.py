# agr hm list se kuch nikalna chahte h to hm remove metod ka use krte h..
# .remove(("name"))
fruits.remove(("melon"))
print(fruits)

# agr hm index k jariye hatana chate h to hm pop method use krte h..
# .pop(index)
fruits.pop(4)
print(fruits)


