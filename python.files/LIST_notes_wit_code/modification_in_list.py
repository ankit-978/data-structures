# agr hm list kuch add krna chahte h to hm append metod usekrte h..
# .append()
fruits=["apple","banana","melon","grapes","mango"]
fruits.append("peach")
print(fruits)


# agr hm list m kuch bech m add krna chahte h to m insert method ka use krte h..
# .insert(index,("name"))
fruits.insert(2,("leach"))
